                        <h1>TIMELINE OF WOVOdat DEVELOPMENT</h1><br>
                        <h3 style="padding:0px 25px 0px 20px;">The first idea</h3>
                        <p style="padding:0px 25px 0px 20px;">
                            After several eruptions in the late 1970's and early 1980's including that of Mount St. Helens (USA) in 1980, C. Newhall (USGS) and H. Okada (Hokkaido University) began planning how to collect, organize and easily access information on volcanic unrest around the world.   They also started discussions with the Smithsonian Institution's Global Volcanism Program (GVP), to interface with information in the GVP database of historical eruptions. Together, WOVOdat and the GVP database are concerned with global occurrences and associations of unrest and eruptions.  Practicing doctors and volcanologists can use epidemiological databases to aid their diagnoses, and volcanology needed a similar database for "volcano epidemiology". But technology at that time was a limiting factor, and the effort was set aside. Early proposal of WOVOdat is here <a href="/about/WOVOdat-proposal-2002.pdf" title="download early proposal" target="_blank">(pdf)</a>.<br>
                        </p>
						<br>

                        <h3 style="padding:0px 25px 0px 20px;">From idea to design</h3>
                        <p style="padding:0px 25px 0px 20px;">
                            The concept of WOVOdat was resurrected as technology advanced. Planning workshops were organized in 2000 and 2002. Principal conclusions from these two workshops; Summary of Bali may be found here <a href="/about/WOVOdat-Bali-2000.pdf" title="download summary" target="_blank">(pdf)</a> and Menlo-Park is here <a href="/about/WOVOdat-Menlo-2002.pdf" title="download summary" target="_blank">(pdf)</a>. 
							Funding was sought and partner institutions identified.  Existing database efforts and tools were reviewed and discussed with their developers.  In the absence of an existing database that could serve the requirements of WOVOdat, Dina Venezky (USGS) took input from these workshops and a number of volunteers and designed WOVOdat 1.0 <a href="/../doc/database/1.0/wovodat10_doc.pdf" target="_blank">(pdf) </a>. 
						<br>
                        </p>
						<br/>
                        <h3 style="padding:0px 25px 0px 20px;">Further phase 2006-2009</h3>
                        <p style="padding:0px 25px 0px 20px;">
                            In November 2006, a steering committee was formed to provide scientific oversight.  Members were drawn from observatories, government agencies, and research groups.<br>
                            The first steering committee meeting was held in December 2006, chaired by WOVO co-chairman Warner Marzocchi (INGV Italy) and Steve Malone (University of Washington).  A technical workshop was held in February 2007, chaired by Florian Schwandner (then at Colorado State Univ) and Steve Malone. Jacopo Selva of INGV (Italy) and Hideki Ueda of NIED (Japan) led a pilot test of WOVOdat 1.0 with small datasets. 
                        </p><br>

