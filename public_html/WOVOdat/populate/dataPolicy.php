<p><h3>Policy re: data use</h3>Out of respect for the time, cost, and difficult work invested by the contributors to collect these data, we require that data users agree to the following rules: 
<ol>
<li>Data from WOVOdat may be used freely for crisis responses, school projects, research and publications that need to compare data from different volcanoes or episodes of unrest. If used for publication about a single volcano, you will acknowledge the owner and cite publication references where available, and offer to collaborate with the data owner(s). </li>
<li>In general, data from WOVOdat should not be used for publicly forecasting the course of current unrest, except by observatories with official mandates for public forecasts in the relevant country. </li>
<li>In citing data from WOVOdat, users should cite the owner(s) of the data, not only WOVOdat. </li>
<li>No user may redistribute or charge for access to data obtained from WOVOdat. Those who use WOVOdat to make value-added products, e.g. risk models, are invited to sponsor further development and maintenance of WOVOdat.</li>
</ol>

