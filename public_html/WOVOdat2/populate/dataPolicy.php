					<div style="text-decoration: underline;font-size:14px;font-weight:bold;">Policy re:  Data contribution</div><br/>
					
					<div>
				    
						1. Data in WOVOdat are owned by the individuals and their organizations that originally collected the data. 
						<br/><br/>

						2. The WOVOdat project will recognize and accept three categories of data:
							<ol type="a">
							<li>Freely available for immediate download.</li>
							<li>Freely available for download after a 2-year grace period (latency).</li>   
							<li>Available only for searches and visualization; downloads not yet available from WOVOdat.</li>
							</ol>
							The project strongly encourages (a) or (b), but offers option (c) if necessary.   Those contributing data in category (c) will be asked to specify a future time when the data may be converted to category (a) or (b).  
						<br/><br/>

						3. Although those who contribute data are generally the owners thereof, the WOVOdat project, WOVO, and IAVCEI cannot assess the validity of such claims.  
						<br/><br/>

					<div style="text-decoration: underline;font-size:14px;font-weight:bold;">Policy re:  Data use</div>	
					<br/>	
						Out of respect for the time, cost, and difficult work invested by the contributors to collect these data, we require that data users agree to the following rules:  
						<br/><br/>
						
						4.	WOVOdat is for research that requires comparisons of data from two or more volcanoes, often from many volcanoes.   Data from WOVOdat may be used freely for crisis responses, school projects, research and publications that need to compare data from different volcanoes or episodes of unrest.   Data on a single volcano or a specific episode of unrest may be used for exploratory research purposes, but if they are to be used for scientific publication, the user must get permission from the original source(s) or the owner(s) of the data.  For any analysis of data from a single volcano, we recommend collaboration with the individual or observatory that contributed the data.
						<br/><br/>

						5.	In general, data from WOVOdat should not be used for publicly forecasting the course of current unrest.   The only exception is use by observatories with official mandates for public forecasts, provided that data from WOVOdat are used as supplements to data from current monitoring by those observatories.
						<br/><br/>

						6.	To encourage responsible use, and to improve the database and utilities, WOVOdat will track and analyse usage.   As a courtesy to contributor(s) of data and to encourage collaboration, WOVOdat will generate an automatic, monthly email to contributor(s) that lists the names and email addresses of those who downloaded their data, and the volcano(s) for which data were accessed.
						<br/><br/>
						
						7.	In citing data from WOVOdat, users should cite the owner(s) of the data, not WOVOdat.  Users may add “Accessed through WOVOdat”.
						<br/><br/>
						
						8.	No user may redistribute or charge for access to data obtained from WOVOdat.    Those who use data from WOVOdat to make value-added products, e.g., risk models, should acknowledge the data owners and WOVOdat. 

