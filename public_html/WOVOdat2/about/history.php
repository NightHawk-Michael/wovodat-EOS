				<h1>TIMELINE OF WOVOdat DEVELOPMENT</h1>
				<h3 style="padding:0px 25px 0px 30px;">The first idea</h3>
				<p style="padding:0px 25px 0px 50px;">
					After several eruptions in the late 1970's and early 1980's including that of Mount St. Helens (USA) in 1980, C. Newhall (USGS) and H. Okada (Hokkaido University) began planning how to collect, organize and easily access information on volcanic unrest around the world.   They also started discussions with the Smithsonian Institution's Global Volcanism Program (GVP), to interface with information in the GVP database of historical eruptions.<br>
					By analogy to medicine, WOVOdat contains data on "symptoms" and the GVP catalogues outcomes.  Together, WOVOdat and the GVP database are concerned with global occurrences and associations of unrest and eruptions.  Practicing doctors and volcanologists can use epidemiological databases to aid their diagnoses, and volcanology needed a similar database for "volcano epidemiology."<br>
					But technology at that time was a limiting factor, and the effort was set aside.<br>
				</p>
				
				
				<h3 style="padding:0px 25px 0px 30px;">From idea to design</h3>
				<p style="padding:0px 25px 0px 50px;">
					The concept of WOVOdat was resurrected as technology advanced. Planning workshops were organized in 2000 and 2002.  Principal conclusions from these two workshops may be found <b>here</b>. Funding was sought and partner institutions identified.  Existing database efforts and tools were reviewed and discussed with their developers.  In the absence of an existing database that could serve the requirements of WOVOdat, Dina Venezky (USGS) took input from these workshops and a number of volunteers and designed WOVOdat 1.0.<br>
				</p>
				<h3 style="padding:0px 25px 0px 30px;">Further phase 2006-2009</h3>
				<p style="padding:0px 25px 0px 50px;">
					In November 2006, a steering committee was formed to provide scientific oversight.  Members were drawn from observatories, government agencies, and research groups.The first &quot;steering committee&quot; meeting was held in December 2006, chaired by WOVO co-chairman Warner Marzocchi (INGV Italy) and Steve Malone (University of Washington).  A technical workshop was held in February 2007, chaired by Florian Schwandner (then at Colorado State Univ) and Steve Malone.   Jacopo Selva of INGV (Italy) and Hideki Ueda of NIED (Japan) led a pilot test of WOVOdat 1.0 with small datasets. 
				</p><br>
				<h3 style="padding:0px 25px 0px 30px;">Principal Conclusions from Bali and Menlo-Park workshops; 2000 & 2002</h3>
				<p style="padding:0px 25px 0px 50px;font-style:italic;">
					&quot;Although WOVO observatories function today without ready access to data from other volcanoes, participants concur that WOVOdat will be very useful, new reference tool for research and crisis response. Nearly all observatories will participate, if the WOVOdat project can minimize time demands on their staff<br>
					The WOVOdat design will be as simple and standard as possible, using open-source database software. We hope to partner with developers of several impressive data visualization software packages that were demonstrated at the Menlo workshop. these end-user programs would be available on the WOVOdat website. We will also encourage users to contribute additional software tools. Several observatories have volunteered to test the database design and data import utilities. Pending funding and the above-mentioned negociations, apilot of the database design could be available for testing by mid-2004. After testing and necessary revision of the database design, the main phase of entering data will begin. WOVOdat will be populated both forward and backward in time. For the past, episodes of unrest will be described together with a brief interval of quiescence to provide baselines. In contrast, most future data will be continuous, to avoid artificial distinctions between unrest and quiescence and to avoid an additional sorting task for the observatories. Long-term hosting and maintenance are still under discussion. Several potential sites and sources of funding were discussed in our palnning workshops and proposals are being prepared.&quot; 
